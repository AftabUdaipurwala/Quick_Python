'''To perform math operations you can use math operators.
The following examples cover all basic math operators (+, -, *, /, **):'''

# Addition
print(1 + 2)

# Subraction
print(4 - 2)

# Multiplication
print(5 * 3)

# Division
print(6 / 2)

# Floor division
print(5 // 2)

# Modulo division
print(5 % 2)

'''Order of operations
The order of operations is (1) exponentiation, (2) multiplication, (3) division, (4) addition, and (5) subtraction.
For example, in the following expression we have exponentiation, multiplication and addition:'''
print(1 + 2 * 10 ** 2)
